# Utiliza la fórmula: Área = (base * altura) / 2


base = 10.5
altura = 7

# Cálculo del área utilizando la fórmula
area = (base * altura) / 2

# Imprimir el resultado del cálculo del área
print("El área del triángulo con base", base, "y altura", altura, "es:", area)

# La fórmula del área de un triángulo se obtiene multiplicando la base por la altura y dividiendo el resultado por 2.

